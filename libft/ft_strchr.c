#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	while (*s != '\0')
	{
		if (*s == c)
			return ((char *)s);
		else
			s++;
	}
	if (c == '\0' && *s == '\0')
		return ((char *)s);
	else
		return (NULL);
}
